    <h1 class="center_f">Sound & Community</h1>
    <p>Music is far more than a simple background noise for the younger generation; it serves as a powerful social architect. As users scroll, they discover that musical judgment is not merely a way to exclude others, but a vital tool for finding one's place in the world.</p>
