    <section>
        <h1>Section 2 : Punk and Hardcore</h1>
        <p>Punk arrived in the mid-1970s as a violent reaction against the "over-sophisticated" rock of the era. By the 1980s, the community radicalized into Hardcore, which functioned as a political movement for youth facing recession.</p>
        <div class="content">
            <img src="img/punk.png" alt="Contenu image">
            <p>Cliché : Punks are just nihilistic vandals with no musical talent.</p>
        </div>
        <p>In reality : The "Do It Yourself" ethic was actually a powerful tool of independence. It taught youth that "anyone could found a band and a label and give concerts in any medium-sized city without signing an unfair contract with a big company".</p>
        <div class="content" id="content-2">
            <img src="img/concertpunk.png" alt="Contenu image">
            <p>Cliché : Concerts are sites of pure, uncontrolled violence.</p>
        </div>
        <p>In reality : Sociological observation shows that while the "pit" is rough, it remains a "controlled discharge of aggressiveness" regulated by a sense of solidarity and shared codes.</p>
    </section>
